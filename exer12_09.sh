# Case logic is often used when many choices are given through a program or when
# many responses can be made on the basis of one choice. In this project, you create a
# shell script that employs case logic to respond to your favorite color (many possible
#responses selected on the basis of one choice). The choices for the user are: blue,
# yellow, red, orange. Print a message of your choice in response to each coloured
# selected by the user

read color

case $color in
red) echo "Red apple. "
blue)
yellow)
*)
esac)